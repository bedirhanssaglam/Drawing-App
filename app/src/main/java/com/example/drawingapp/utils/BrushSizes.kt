package com.example.drawingapp.utils

/**
 * Enum class representing different brush sizes for drawing.
 * Each enum constant holds a corresponding size value in pixels.
 */
enum class BrushSizes(val size: Float) {
    SMALL(10f),   // Small brush size with a diameter of 10 pixels
    MEDIUM(20f),  // Medium brush size with a diameter of 20 pixels
    LARGE(30f)    // Large brush size with a diameter of 30 pixels
}
